package sysos;

public class ImprimirPantalla {

	/**
	 * Metodo que imprime "consola :" y a continuacion imprime la cadena de texto 
	 * pasada por parametro.
	 * @param s La cadena de texto que se quiere imprimir
	 * @author UpgradeHub
	 */
	public void imprimir(String s) {
		System.out.println("consola: " + s);
	}
	
	/**
	 * Metodo que imprime "consola :" y a continuacion imprime el numero 
	 * pasado por parametro.
	 * @param n El entero que se quiere imprimir
	 * @author UpgradeHub
	 */
	public void imprimir(int n) {
		System.out.println("consola :" + n);
	}
	
}
